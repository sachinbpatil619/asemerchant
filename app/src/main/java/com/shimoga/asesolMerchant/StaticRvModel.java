package com.shimoga.asesolMerchant;

public class StaticRvModel {
    private int img;
    private String text;

    public StaticRvModel(int img, String text) {
        this.img = img;
        this.text = text;
    }

    public int getImg() {
        return img;
    }

    public String getText() {
        return text;
    }

}
